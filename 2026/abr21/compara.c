#include <stdio.h>

int main(void) {

  // Menor
  printf("10 < 20: %d\n", 10 < 20);

  // Mayor
  printf("10 > 20: %d\n", 10 > 20);

  // Mayor igual
  printf("10 >= 20: %d\n", 10 >= 20);
  printf("20 >= 20: %d\n", 20 >= 20);

  // Igual
  printf("10 == 20: %d\n", 10 == 20);
  printf("20 == 20: %d\n", 20 == 20);

  // Distinto
  printf("10 != 20: %d\n", 10 != 20);
  printf("20 != 20: %d\n", 20 != 20);

  return 0;
}
