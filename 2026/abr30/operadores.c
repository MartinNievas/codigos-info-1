#include <stdio.h>

int main(void) {

  printf("Suma: %d\n", 30+40);
  printf("Resta: %d\n", 30-40);
  printf("Cociente: %d\n", 30/40);
  printf("Producto: %d\n", 30*40);
  printf("Resto: %d\n", 30%40);

  printf("Lógicos\n");
  printf("Mayor: %d\n", 30 > 40);
  printf("Menor: %d\n", 30 < 40);

  return 0;
}
